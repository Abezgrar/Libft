#include "libft.h"

void		ft_striter(char *s, void (*f)(char *))
{
	int		index;
	int		i;

	if (s && f)
	{
		index = ft_strlen(s);
		i = 0;
		while (i < index)
		{
			f(&s[i]);
			i++;
		}
	}
}
